public class Ejercicio_08 {
    
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        int resultado = a / b;

        System.out.println("Resultado: " + resultado);
    }
}

/*
En Java, cuando se divide entre dos números enteros (int),
el resultado también es entero.

5 / 2 = 2.5 

Pero Java corta la parte decimal
y deja solo el número entero: 2.
*/